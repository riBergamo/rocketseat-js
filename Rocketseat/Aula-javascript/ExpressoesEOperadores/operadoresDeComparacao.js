//Operadores de comparação
//irá comparar valores e retornar um boolean.

let one = 1
let two = 2

// igual a (==) 
console.log(two == 1) //return false
console.log(one == '1')

// diferente de (!=)
console.log(one != two)
console.log(one != 1)
console.log(two != two)



//ESTRITAMENTE igual a (===) 
console.log(one === '1')
console.log(one === 1)

//ESTRITAMENTE diferente de (!==)
console.log(two !== '2')
console.log(two !== 2)
console.log(two !== 1)



// maior que (>)
console.log(one > two)

// maior/igual a (>=)
console.log(one >= two)

// menor que (<)
console.log(one < two)

// menor/igual a (<=)
console.log(one <= two)
