/*
FALSY:
 - é um valor  
 - quando um valor é considerado FAlSE em contextos onde um booleano é obrigátorio(condicionais e loops)
*/


/*
Pode ser considerado um FALSY quando a condição é:
(Dão falsos quando usados)
 - false
 - 0
 - -0
 - "" (string vazia)
 - null
 - undefined
 - NaN 
*/
console.log(false?'verdadeiro':'falso')

/*
TRUTHY:
 - quando um valor é considerado TRUE em contextos onde um booleano é obrigátorio(condicionais e loops)
*/ 

/*
Pode ser considerado um TRUTHY quando a condição é:
(Dão verdadeiros quando usados)
 - true
 - {}
 - []
 - 1
 - 3.23
 - "0"
 - "false"
 - -1
 - Infinity
 - -Infinity
*/
console.log( [] ?'verdadeiro':'falso')



